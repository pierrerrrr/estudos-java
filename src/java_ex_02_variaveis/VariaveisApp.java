package java_ex_02_variaveis;

public class VariaveisApp {
    public static void main(String[] args) {

        // o que define que a variavel não será mudada é o préfixo "final"
        final String name = "Iago";
        String lastName = "Pierre Storniolo";
        int age = 24;
        double bankAccount = 100.0;
    }
}
